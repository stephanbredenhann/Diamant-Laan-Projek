function buildLabel(entry: RawCountryCode): string {
  // Keep the historical "ZA (+27)" style for South Africa; other entries use the full country name.
  if (entry.country === 'South Africa') return `ZA (${entry.code})`;
  return `${entry.country} (${entry.code})`;
}

const southAfrica = RAW_COUNTRY_CODES.find((c) => c.country === 'South Africa')!;
const rest = RAW_COUNTRY_CODES.filter((c) => c.country !== 'South Africa').sort((a, b) =>
  a.country.localeCompare(b.country)
);

export const ALL_COUNTRY_CODES: CountryCode[] = [southAfrica, ...rest].map((entry) => ({
  ...entry,
  label: buildLabel(entry),
}));
